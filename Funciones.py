#resta
def Resta(x,y):
    resultado=x-y
    return resultado

#multiplicacion
def Multiplicacion(x,y):
    resultado=x*y
    return resultado

#division
def division(a,b):
    resultado= a/b
    return resultado 

#suma
def Suma(x,y):
    resultado=x+y
    return resultado

#sumatoria
def sumatoria (lista):
    resultado=0
    for index in lista:
        resultado= resultado+index 
    return (resultado)